package project;

import java.util.Properties;
import javafx.application.Application;
import javafx.event.ActionEvent;
import javafx.event.EventHandler;
import javafx.scene.Scene;
import javafx.scene.control.Button;
import javafx.scene.layout.StackPane;
import javafx.stage.Stage;
import javax.mail.Message;
import javax.mail.MessagingException;
import javax.mail.Session;
import javax.mail.PasswordAuthentication;
import javax.mail.Transport;
import javax.mail.internet.AddressException;
import javax.mail.internet.InternetAddress;
import javax.mail.internet.MimeMessage;

/**
 *
 * @author Crow
 */
public class Project extends Application {
    
    @Override
    public void start(Stage primaryStage) throws AddressException, MessagingException {
        Button btn = new Button();
        btn.setText("Say 'Hello World'");
        btn.setOnAction(new EventHandler<ActionEvent>() {
            
            @Override
            public void handle(ActionEvent event) {
                System.out.println("Hello World!");
            }
        });
        
        StackPane root = new StackPane();
        root.getChildren().add(btn);
        
        Scene scene = new Scene(root, 300, 250);
        
        primaryStage.setTitle("Hello World!");
        primaryStage.setScene(scene);
        primaryStage.show();
        
        //mailing API
        final String username ="youssefxhaffar@gmail.com";
        final String password ="animelover3";
        String fromEmail ="youssefxhaffar@gmail.com";
        String toEmail ="mohamedyoussef.haffar@esprit.tn";
        
        Properties properties =new Properties();
        properties.put("mail.smtp.auth", "true");
        properties.put("mail.smtp.starttls.enable", "true");
        properties.put("mail.smtp.host", "smtp.gmail.com");
        properties.put("mail.smtp.port", "587");
        
        Session session =Session.getDefaultInstance(properties, new javax.mail.Authenticator() {
              protected PasswordAuthentication getPasswordAuthentication()
              {
                  return new PasswordAuthentication(username, password);
              }
        });
        
        //Start our mail message
        
        MimeMessage msg = new MimeMessage(session);
        try {
            msg.setFrom(new InternetAddress(fromEmail));
            msg.addRecipient(Message.RecipientType.TO, new InternetAddress(toEmail));
            msg.setText("Email body text");
            Transport.send(msg);
            System.out.println("Sent Message");
            
        } catch (MessagingException e) {
            e.printStackTrace();
        }
        
        
        
        
        
    }

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        launch(args);
    }
    
}
